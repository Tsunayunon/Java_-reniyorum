public class for_while {
    public static void main(String[] args) {
    //!for döngüsü:
        //? Döngü i 1'den başlar ve 10'a kadar (10 dahil) devam eder
        for (int i = 1; i <= 10; i++) {
            System.out.println(i); //? Döngü her çalıştığında "i" yazdırır
        }
        System.out.println("****FOR DÖNGÜSÜ BİTTİ******");
    //!while döngüsü:
        int i = 1;
        while (i<=20) {
            System.out.println(i);
            i++;

    }
    System.out.println("****WHİLE DÖNGÜSÜ BİTTİ******");
}
}
